    private void cmbtujuanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmbtujuanActionPerformed
        // TODO add your handling code here:
        if (cmbtujuan.getSelectedItem().equals("---Tujuan---")) {
            buttonGroup1.clearSelection();
            txtharga.setText("");
        } else if (cmbtujuan.getSelectedItem().equals("Pontianak - Jakarta")){
            hdewasa = 900000;
            hanak   = 450000;
        } else if (cmbtujuan.getSelectedItem().equals("Jakarta - Tokyo")){
            hdewasa = 2000000;
            hanak   = 1500000;
        } else if (cmbtujuan.getSelectedItem().equals("Pontianak - Yogyakarta")){
            hdewasa = 950000;
            hanak   = 500000;
        } else if (cmbtujuan.getSelectedItem().equals("Yogyakarta - Jakarta")) {
            hdewasa = 875000;
            hanak   = 450000;
        } else if (cmbtujuan.getSelectedItem().equals("Pontianak - Batam")){
            hdewasa = 1250000;
            hanak   = 650000;
        } else if (cmbtujuan.getSelectedItem().equals("Batam - Singapore")){
            hdewasa = 980000;
            hanak   = 475000;
        } else if (cmbtujuan.getSelectedItem().equals("Jakarta - Medan")){
            hdewasa = 600000;
            hanak   = 350000;
        } else if (cmbtujuan.getSelectedItem().equals("Jakarta - Palembang")) {
            hdewasa = 500000;
            hanak   = 300000;
        } else if (cmbtujuan.getSelectedItem().equals("Palembang - Pontianak")) {
            hdewasa = 750000;
            hanak   = 300000;
        }
    }//GEN-LAST:event_cmbtujuanActionPerformed

