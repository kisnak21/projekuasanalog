    private void prosesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_prosesActionPerformed
        // TODO add your handling code here:
        beli = Integer.parseInt(txtjumlah.getText());
        harga = Integer.parseInt(txtharga.getText());
        total = beli * harga;
        txtbeli.setText(String.valueOf(total));
        JOptionPane.showMessageDialog(null, "Pesanan Anda Akan Segera Di Proses!\n\nTotal yang harus dibayar =Rp "+total);
        
        txtarea.setText(
                "Nama                           : "+txtnama.getText()+ "\n\n" +
                "Nama Maskapai          : "+txtmaskapai.getText()+"\n\n" +
                "Harga Tiket                  : Rp "+txtbeli.getText()+"\n\n" +
                "Jumlah Tiket               : "+txtjumlah.getText()+"\n\n" +
                "\n" +
                "\n" +
                "Semoga Perjalanan anda menyenangkan!"+"\n");
        
        
        
        
        
        
    }//GEN-LAST:event_prosesActionPerformed

