    private void cmbkodeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmbkodeActionPerformed
        // TODO add your handling code here:
        if (cmbkode.getSelectedItem().equals("LNI")){
            buttonGroup1.clearSelection();
            txtmaskapai.setText("LION AIR");
            cmbtujuan.removeAllItems();
            cmbtujuan.addItem("---Tujuan---");
            cmbtujuan.addItem("Pontianak - Jakarta");
            cmbtujuan.addItem("Jakarta - Tokyo");
            cmbtujuan.addItem("Pontianak - Yogyakarta");
        } else if 
            (cmbkode.getSelectedItem().equals("CTV")){
            txtmaskapai.setText("Citilink");
            cmbtujuan.removeAllItems();
            cmbtujuan.addItem("---Tujuan---");
            cmbtujuan.addItem("Yogyakarta - Jakarta");
            cmbtujuan.addItem("Pontianak - Batam");
            cmbtujuan.addItem("Batam - Singapore");   
        } else if (cmbkode.getSelectedItem().equals("SJY")){
            txtmaskapai.setText("Sriwijaya Air");
            cmbtujuan.addItem("---Tujuan---");
            cmbtujuan.addItem("Jakarta - Medan");
            cmbtujuan.addItem("Jakarta - Palembang");
            cmbtujuan.addItem("Palembang - Pontianak");
        } else if (cmbkode.getSelectedItem().equals("---PILIH KODE---")){
            txtmaskapai.setText("");
            cmbtujuan.removeAllItems();
        }
    }//GEN-LAST:event_cmbkodeActionPerformed

